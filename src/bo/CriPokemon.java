package bo;

public class CriPokemon {
    public String criV;
    public String criP;
    public String criC;

    public CriPokemon(String criV,String criP,String criC) {
        this.criC = criC;
        this.criP = criP;
        this.criV = criV;
    }

    public String getCriV() {
        return this.criV;
    }

    public String getCriP() {
        return this.criP;
    }

    public String getCriC() {
        return this.criC;
    }
}
















